package org.example

fun main() {
    println("##Assignment 1: Immutable Variable (val)")
    val daysInWeek = 7;
    println("Immutable variable: $daysInWeek")


    println("##Assignment 2: Mutable Variable (var)")

    var userScore: Int = 0;
    var output = userScore + 10;
    println("Mutable variable: $output")


    println("##Assignment 3: Arithmetic Operators")
    val width = 5
    val height = 10
    val area = width * height
    println("Arithmetic Operators: $area")

}